package com;

public class _02_NestedIf {
    public static void main(String[] args) {
        double say=-4;

        if (say != 0) {
            if (say >0) {
                System.out.println("Sonuç = "+(1/say));
            }else
            {
                System.out.println("Pozitif Sayı Gerekli");
            }

        }

    }
}
