package com;

public class FloatingPointLiteralleri {
    public static void main(String[] args) {

        double d1=123.4;
        double d2=1.234e2;//',' den sonra kaç basamak kaaydıracağımızı söyler.10*10 =e2
        float f1=123.4f;

        System.out.println("normal :"+d1);
        System.out.println("e  :"+d2);
        System.out.println("f   :"+f1);

    }
}
